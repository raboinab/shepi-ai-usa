/**
 * Build the ChatGPT widget bundle and embed it into the chatgpt-mcp Edge Function.
 *
 * 1. Bundles src/chatgpt-widgets/project-summary.html into a single HTML file.
 * 2. Reads the bundled HTML.
 * 3. Writes a TypeScript module exporting the HTML as a string constant.
 * 4. The Edge Function imports the module and serves the HTML via registerAppResource.
 */

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const WIDGET_DIR = resolve(ROOT, "src/chatgpt-widgets");
const DIST_DIR = resolve(ROOT, "dist/chatgpt-widgets");
const OUTPUT_FILE = resolve(ROOT, "supabase/functions/chatgpt-mcp/widgets/projectSummaryHtml.ts");

console.log("Building ChatGPT widget bundle...");
execSync("npx vite build --config vite.config.chatgpt-widgets.ts", { cwd: ROOT, stdio: "inherit" });

const htmlPath = resolve(DIST_DIR, "project-summary.html");
const html = readFileSync(htmlPath, "utf-8");

// Escape backticks and ${} in the HTML so it is safe inside a template literal.
const escaped = html
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\$/g, "\\$");

mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
writeFileSync(
  OUTPUT_FILE,
  `// AUTO-GENERATED by scripts/build-chatgpt-widgets.mjs — do not edit manually.\nexport const PROJECT_SUMMARY_HTML = \`${escaped}\`;\n`,
  "utf-8",
);

console.log(`Embedded widget HTML into ${OUTPUT_FILE} (${html.length} chars)`);
